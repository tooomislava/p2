class Muskarac {
    public String sifra;
    public boolean asocijalno;
    public String modelNaocala;
    public int ogrlica;
    public String bojaOciju;
    public String carape;

    public String getSifra() {
        return sifra;
    }

    public void setSifra(String sifra) {
        this.sifra = sifra;
    }

    public boolean isAsocijalno() {
        return asocijalno;
    }

    public void setAsocijalno(boolean asocijalno) {
        this.asocijalno = asocijalno;
    }

    public String getModelNaocala() {
        return modelNaocala;
    }

    public void setModelNaocala(String modelNaocala) {
        this.modelNaocala = modelNaocala;
    }

    public int getOgrlica() {
        return ogrlica;
    }

    public void setOgrlica(int ogrlica) {
        this.ogrlica = ogrlica;
    }

    public String getBojaOciju() {
        return bojaOciju;
    }

    public void setBojaOciju(String bojaOciju) {
        this.bojaOciju = bojaOciju;
    }

    public String getCarape() {
        return carape;
    }

    public void setCarape(String carape) {
        this.carape = carape;
    }
    
}

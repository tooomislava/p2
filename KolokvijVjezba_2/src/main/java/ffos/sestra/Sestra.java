package ffos.sestra;

/**
 *
 * @author Jure
 */
public class Sestra {

    public String sifra;
    public int narukvica;
    public float novcici;
    public float kuna;
    public String bojaKose;
    public int nausnica;
    public Svekar svekar;

    public String getSifra() {
        return sifra;
    }

    public void setSifra(String sifra) {
        this.sifra = sifra;
    }

    public int getNarukvica() {
        return narukvica;
    }

    public void setNarukvica(int narukvica) {
        this.narukvica = narukvica;
    }

    public float getNovcici() {
        return novcici;
    }

    public void setNovcici(float novcici) {
        this.novcici = novcici;
    }

    public float getKuna() {
        return kuna;
    }

    public void setKuna(float kuna) {
        this.kuna = kuna;
    }

    public String getBojaKose() {
        return bojaKose;
    }

    public void setBojaKose(String bojaKose) {
        this.bojaKose = bojaKose;
    }

    public int getNausnica() {
        return nausnica;
    }

    public void setNausnica(int nausnica) {
        this.nausnica = nausnica;
    }

    public Svekar getSvekar() {
        return svekar;
    }

    public void setSvekar(Svekar svekar) {
        this.svekar = svekar;
    }

}

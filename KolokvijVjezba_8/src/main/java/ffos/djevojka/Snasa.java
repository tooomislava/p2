package ffos.djevojka;

/**
 *
 * @author Jure
 */
class Snasa {
    
    public int sifra;
    public String vesta;
    public float euro;
    public String bojaKose;
    public String haljina;
    public int nausnica;

    public int getSifra() {
        return sifra;
    }

    public void setSifra(int sifra) {
        this.sifra = sifra;
    }

    public String getVesta() {
        return vesta;
    }

    public void setVesta(String vesta) {
        this.vesta = vesta;
    }

    public float getEuro() {
        return euro;
    }

    public void setEuro(float euro) {
        this.euro = euro;
    }

    public String getBojaKose() {
        return bojaKose;
    }

    public void setBojaKose(String bojaKose) {
        this.bojaKose = bojaKose;
    }

    public String getHaljina() {
        return haljina;
    }

    public void setHaljina(String haljina) {
        this.haljina = haljina;
    }

    public int getNausnica() {
        return nausnica;
    }

    public void setNausnica(int nausnica) {
        this.nausnica = nausnica;
    }
    
}

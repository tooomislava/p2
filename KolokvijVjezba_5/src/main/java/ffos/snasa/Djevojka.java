package ffos.snasa;

import java.util.Date;

/**
 *
 * @author Jure
 */
class Djevojka {

    public int sifra;
    public String bojaOciju;
    public String carape;
    public Date drugiPuta;
    public int prsten;
    public int nausnica;

    public int getSifra() {
        return sifra;
    }

    public void setSifra(int sifra) {
        this.sifra = sifra;
    }

    public String getBojaOciju() {
        return bojaOciju;
    }

    public void setBojaOciju(String bojaOciju) {
        this.bojaOciju = bojaOciju;
    }

    public String getCarape() {
        return carape;
    }

    public void setCarape(String carape) {
        this.carape = carape;
    }

    public Date getDrugiPuta() {
        return drugiPuta;
    }

    public void setDrugiPuta(Date drugiPuta) {
        this.drugiPuta = drugiPuta;
    }

    public int getPrsten() {
        return prsten;
    }

    public void setPrsten(int prsten) {
        this.prsten = prsten;
    }

    public int getNausnica() {
        return nausnica;
    }

    public void setNausnica(int nausnica) {
        this.nausnica = nausnica;
    }

}

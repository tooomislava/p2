package ffos.ostavljen;

/**
 *
 * @author Jure
 */
class Neprijatelj {

    private int sifra;
    private int narukvica;
    private String hlace;
    private String bojaOciju;
    private String stilFrizure;
    private String suknja;

    public int getSifra() {
        return sifra;
    }

    public void setSifra(int sifra) {
        this.sifra = sifra;
    }

    public int getNarukvica() {
        return narukvica;
    }

    public void setNarukvica(int narukvica) {
        this.narukvica = narukvica;
    }

    public String getHlace() {
        return hlace;
    }

    public void setHlace(String hlace) {
        this.hlace = hlace;
    }

    public String getBojaOciju() {
        return bojaOciju;
    }

    public void setBojaOciju(String bojaOciju) {
        this.bojaOciju = bojaOciju;
    }

    public String getStilFrizure() {
        return stilFrizure;
    }

    public void setStilFrizure(String stilFrizure) {
        this.stilFrizure = stilFrizure;
    }

    public String getSuknja() {
        return suknja;
    }

    public void setSuknja(String suknja) {
        this.suknja = suknja;
    }

}

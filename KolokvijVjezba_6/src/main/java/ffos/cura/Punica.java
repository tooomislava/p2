class Punica {
    
    public int sifra;
    public String vesta;
    public boolean introvertno;
    public String hlace;
    public String kratkMajica;
    public boolean asocijalno;

    public int getSifra() {
        return sifra;
    }

    public void setSifra(int sifra) {
        this.sifra = sifra;
    }

    public String getVesta() {
        return vesta;
    }

    public void setVesta(String vesta) {
        this.vesta = vesta;
    }

    public boolean isIntrovertno() {
        return introvertno;
    }

    public void setIntrovertno(boolean introvertno) {
        this.introvertno = introvertno;
    }

    public String getHlace() {
        return hlace;
    }

    public void setHlace(String hlace) {
        this.hlace = hlace;
    }

    public String getKratkMajica() {
        return kratkMajica;
    }

    public void setKratkMajica(String kratkMajica) {
        this.kratkMajica = kratkMajica;
    }

    public boolean isAsocijalno() {
        return asocijalno;
    }

    public void setAsocijalno(boolean asocijalno) {
        this.asocijalno = asocijalno;
    }
    
}
